#!/usr/bin/env node

const { execFileSync } = require('node:child_process');
const fs = require('node:fs');
const fsp = require('node:fs/promises');
const os = require('node:os');
const path = require('node:path');
const assert = require('node:assert/strict');

const ROOT = path.resolve(__dirname, '..');

async function writeFile(filePath, content) {
  await fsp.mkdir(path.dirname(filePath), { recursive: true });
  await fsp.writeFile(filePath, content, 'utf8');
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

async function createFixture(root) {
  const declarations = [
    '// AUTO-GENERATED - do not edit.',
    '// Generated by dts-bundle-generator.',
    'declare function box(width: number, depth: number, height: number): Shape;',
    'declare interface Shape { color(value: string): Shape; }',
  ];
  for (let i = 0; i < 2500; i++) {
    declarations.push(`declare function recoveredApi${i}(value: number): string;`);
  }
  const forgeTypesLiteral = JSON.stringify(declarations.join('\n'));
  const contextMdLiteral = JSON.stringify(Array.from({ length: 2500 }, (_, index) => `# Context ${index}\nRecovered runtime note ${index}.`).join('\n'));

  const source = [
    `const FORGE_TYPES = ${forgeTypesLiteral};`,
    `const contextMd = ${contextMdLiteral};`,
    'function CodeEditor() {',
    '  const theme = useForgeStore(s => s.theme);',
    '  const editorRef = { current: null };',
    '  const handleMount = (editor, monaco) => {',
    '    editorRef.current = editor;',
    '    console.info(contextMd.length);',
    '    monaco.languages.typescript.javascriptDefaults.addExtraLib(FORGE_TYPES, "forge.d.ts");',
    '    monaco.languages.typescript.javascriptDefaults.setDiagnosticsOptions({',
    '      noSemanticValidation: true,',
    '      noSyntaxValidation: false',
    '    });',
    '    monaco.editor.defineTheme("forge-gruvbox", { base: "vs-dark", inherit: true, rules: [], colors: {} });',
    '    editor.addCommand(monaco.KeyMod.CtrlCmd | monaco.KeyCode.KeyS, () => {',
    '      useForgeStore.getState().saveFile();',
    '    });',
    '  };',
    '  return jsxRuntimeExports.jsx(Editor, {',
    '    theme: theme === "gruvbox" ? "forge-gruvbox" : theme === "light" ? "light" : "vs-dark",',
    '    onMount: handleMount',
    '  });',
    '}',
    'export { CodeEditor };',
    '',
  ].join('\n');

  await writeFile(path.join(root, 'src/recovered-parts/EditorApp-test/state-management.js'), source);
  await writeFile(path.join(root, 'src/recovered-entry/EditorApp-test.js'), source);
}

async function main() {
  const tempRoot = await fsp.mkdtemp(path.join(os.tmpdir(), 'jsmap-runtime-patch-'));
  await createFixture(tempRoot);

  execFileSync(
    process.execPath,
    [
      path.join(ROOT, 'scripts/jsmap.cjs'),
      'runtime-patch',
      tempRoot,
      '--min-payload-kb',
      '20',
    ],
    { stdio: 'pipe' },
  );

  const plan = readJson(path.join(tempRoot, 'runtime-replacement-plan.json'));
  assert.equal(plan.generatedBy, 'jsmap runtime-patch');
  assert(plan.summary.extractablePayloads >= 1, 'plan should find the large declaration payload');
  assert(plan.summary.payloadExtractionActions >= 1, 'plan should create standalone payload extraction actions');
  assert(plan.summary.replacementCandidates >= 1, 'plan should find the Monaco mount replacement candidate');

  const payload = plan.extractablePayloads.find((item) => item.symbol === 'FORGE_TYPES');
  assert(payload, 'FORGE_TYPES should be an extractable payload');
  assert(payload.evidence.includes('type-declaration-payload'), 'payload should include declaration evidence');
  assert(plan.extractablePayloads.some((item) => item.symbol === 'contextMd'), 'contextMd should be an extractable payload');

  const candidate = plan.replacementCandidates.find((item) => item.runtime === 'monaco-editor');
  assert(candidate, 'Monaco runtime candidate should be present');
  assert(candidate.evidence.includes('monaco-extra-lib'), 'candidate should include Monaco extra-lib evidence');
  assert(candidate.actions.some((action) => action.type === 'replace-inline-function'), 'candidate should include function replacement action');
  assert(candidate.actions.some((action) => action.type === 'replace-inline-theme-resolver'), 'candidate should include theme resolver replacement action');
  assert(candidate.actions.some((action) => action.type === 'extract-inline-payload' && action.payloadSymbol === 'FORGE_TYPES'), 'candidate should include payload extraction action');
  assert(candidate.actions.some((action) => action.type === 'extract-inline-payload' && action.payloadSymbol === 'contextMd'), 'candidate should include contextMd payload extraction action');
  assert(candidate.adapter.suggestedFile === 'src/editor/recovered-monaco-editor-adapter.js', 'candidate should suggest editor adapter path');

  const markdown = fs.readFileSync(path.join(tempRoot, 'runtime-replacement-plan.md'), 'utf8');
  assert(markdown.includes('# Runtime Replacement Plan'), 'markdown report should be written');
  assert(markdown.includes('replace-inline-function'), 'markdown should list replacement actions');

  execFileSync(
    process.execPath,
    [
      path.join(ROOT, 'scripts/jsmap.cjs'),
      'runtime-patch',
      tempRoot,
      '--apply',
    ],
    { stdio: 'pipe' },
  );

  const dryRunManifest = readJson(path.join(tempRoot, 'runtime-patch-manifest.dry-run.json'));
  assert.equal(dryRunManifest.mode, 'dry-run');
  assert(dryRunManifest.summary.wouldApply >= 1, 'dry-run manifest should identify applicable actions');
  assert(fs.readFileSync(path.join(tempRoot, 'src/recovered-parts/EditorApp-test/state-management.js'), 'utf8').includes('const FORGE_TYPES = '), 'dry-run should not edit source files');
  assert(!fs.existsSync(path.join(tempRoot, 'src/editor/forge-types.js')), 'dry-run should not write extracted modules');

  const corruptPlan = structuredClone(plan);
  corruptPlan.replacementCandidates[0].actions[0].before.exactHash = '0000000000000000';
  await writeFile(path.join(tempRoot, 'runtime-replacement-plan-corrupt.json'), `${JSON.stringify(corruptPlan, null, 2)}\n`);
  execFileSync(
    process.execPath,
    [
      path.join(ROOT, 'scripts/jsmap.cjs'),
      'runtime-patch',
      tempRoot,
      '--apply',
      '--plan',
      path.join(tempRoot, 'runtime-replacement-plan-corrupt.json'),
      '--manifest',
      path.join(tempRoot, 'runtime-patch-manifest-corrupt.json'),
    ],
    { stdio: 'pipe' },
  );
  const corruptManifest = readJson(path.join(tempRoot, 'runtime-patch-manifest-corrupt.json'));
  assert(corruptManifest.actions.some((action) => action.reason === 'plan-before-hash-mismatch'), 'apply mode should skip actions with invalid before hashes');

  execFileSync(
    process.execPath,
    [
      path.join(ROOT, 'scripts/jsmap.cjs'),
      'runtime-patch',
      tempRoot,
      '--write',
    ],
    { stdio: 'pipe' },
  );

  const writeManifest = readJson(path.join(tempRoot, 'runtime-patch-manifest.json'));
  assert.equal(writeManifest.mode, 'write');
  assert(writeManifest.summary.applied >= 1, 'write manifest should record applied actions');
  assert(writeManifest.moduleWrites.some((item) => item.file === 'src/editor/forge-types.js'), 'write manifest should list FORGE_TYPES module');
  assert(writeManifest.moduleWrites.some((item) => item.file === 'src/editor/context-md.js'), 'write manifest should list contextMd module');

  const patchedSource = fs.readFileSync(path.join(tempRoot, 'src/recovered-parts/EditorApp-test/state-management.js'), 'utf8');
  assert(patchedSource.includes('import { FORGE_TYPES } from "../../editor/forge-types.js";'), 'FORGE_TYPES declaration should become a relative import');
  assert(patchedSource.includes('import { contextMd } from "../../editor/context-md.js";'), 'contextMd declaration should become a relative import');
  assert(patchedSource.includes('createRecoveredMonacoEditorMountHandler'), 'inline mount handler should be replaced');
  assert(fs.readFileSync(path.join(tempRoot, 'src/editor/forge-types.js'), 'utf8').startsWith('export const FORGE_TYPES = '), 'FORGE_TYPES module should be written');
  assert(fs.readFileSync(path.join(tempRoot, 'src/editor/context-md.js'), 'utf8').startsWith('export const contextMd = '), 'contextMd module should be written');

  const generatedRoot = await fsp.mkdtemp(path.join(os.tmpdir(), 'jsmap-runtime-patch-generated-'));
  await createFixture(generatedRoot);
  await writeFile(path.join(generatedRoot, 'scripts/rebuild-project.cjs'), '#!/usr/bin/env node\n');
  await writeFile(path.join(generatedRoot, 'scripts/link-recovered-assets.mjs'), [
    "import fs from 'node:fs/promises';",
    "import path from 'node:path';",
    "import { fileURLToPath } from 'node:url';",
    '',
    "const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');",
    "const outDir = path.join(root, 'src/recovered-entry');",
    'await fs.mkdir(outDir, { recursive: true });',
    '',
    "for (const entry of ['EditorApp-test.js']) {",
    '  const parts = [];',
    "  parts.push(await fs.readFile(path.join(root, 'src/recovered-parts/EditorApp-test/state-management.js'), 'utf8'));",
    "  await fs.writeFile(path.join(outDir, entry), parts.join('\\n') + '\\n', 'utf8');",
    '}',
    '',
  ].join('\n'));
  execFileSync(
    process.execPath,
    [
      path.join(ROOT, 'scripts/jsmap.cjs'),
      'runtime-patch',
      generatedRoot,
      '--min-payload-kb',
      '20',
    ],
    { stdio: 'pipe' },
  );
  execFileSync(
    process.execPath,
    [
      path.join(ROOT, 'scripts/jsmap.cjs'),
      'runtime-patch',
      generatedRoot,
      '--write',
      '--build-check',
      '--browser-smoke-command',
      'npm run preview -- --port 4177',
    ],
    { stdio: 'pipe' },
  );
  const generatedManifest = readJson(path.join(generatedRoot, 'runtime-patch-manifest.json'));
  assert(generatedManifest.generatedEntryLocked, 'manifest should record generated-entry lock');
  assert(generatedManifest.linker.applyViaLinker, 'generated workspace should apply through the linker');
  assert.equal(generatedManifest.buildCheck.ok, false, 'fixture build-check should run and record failure without aborting manifest writes');
  assert.equal(generatedManifest.browserSmoke.command, 'npm run preview -- --port 4177', 'manifest should record browser smoke command');
  assert(generatedManifest.changedFiles.some((item) => item.file === 'scripts/link-recovered-assets.mjs'), 'write manifest should list the patched linker');
  assert(generatedManifest.actions.some((action) => action.applyTarget === 'linker'), 'runtime actions should target the linker');
  assert(fs.readFileSync(path.join(generatedRoot, 'src/recovered-parts/EditorApp-test/state-management.js'), 'utf8').includes('const FORGE_TYPES = '), 'recovered parts should not be edited when applying through linker');
  execFileSync(process.execPath, [path.join(generatedRoot, 'scripts/link-recovered-assets.mjs')], { stdio: 'pipe' });
  const linkedEntry = fs.readFileSync(path.join(generatedRoot, 'src/recovered-entry/EditorApp-test.js'), 'utf8');
  assert(linkedEntry.includes('import { FORGE_TYPES } from "../editor/forge-types.js";'), 'linker should patch FORGE_TYPES into generated entry');
  assert(linkedEntry.includes('import { contextMd } from "../editor/context-md.js";'), 'linker should patch contextMd into generated entry');

  console.log(`Runtime patch fixture passed: ${tempRoot}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.stack || error.message : error);
  process.exitCode = 1;
});
