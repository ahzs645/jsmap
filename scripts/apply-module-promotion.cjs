#!/usr/bin/env node

/**
 * Generate starter promotion files from recovery-promotion-plan.json.
 *
 * Default mode is a dry-run preview: files are written under
 * .jsmap-promote-preview/ and the runnable linked rebuild is not changed.
 * Use --write to write the suggested src/promoted/* files.
 */

const fs = require('node:fs');
const fsp = require('node:fs/promises');
const path = require('node:path');

const VALID_ACTIONS = new Set([
  'create-export-facade',
  'create-runtime-export-facade',
  'create-scope-wrapper',
  'extract-leaf-module',
  'wrap-runtime-boundary',
  'wrap-or-replace-vendor',
  'inspect-only',
]);

const ACTION_ALIASES = new Map([
  ['facade', 'create-export-facade'],
  ['runtime', 'wrap-runtime-boundary'],
  ['wrapper', 'create-scope-wrapper'],
  ['scope', 'create-scope-wrapper'],
  ['leaf', 'extract-leaf-module'],
  ['vendor', 'wrap-or-replace-vendor'],
  ['inspect', 'inspect-only'],
]);

function printUsage() {
  console.error('Usage: jsmap promote-apply <linked-rebuild-dir> [--dry-run|--write] [--limit N] [--actions a,b,c] [--out <preview-dir>] [--build-check]');
  console.error(`Actions: ${[...VALID_ACTIONS].join(', ')}`);
  console.error('Aliases: facade, runtime, wrapper, scope, leaf, vendor, inspect');
}

function parseActionList(value) {
  const actions = new Set();
  const unknown = [];
  for (const raw of String(value).split(',').map((item) => item.trim()).filter(Boolean)) {
    const action = ACTION_ALIASES.get(raw) || raw;
    if (!VALID_ACTIONS.has(action)) {
      unknown.push(raw);
      continue;
    }
    actions.add(action);
  }
  if (unknown.length > 0) {
    throw new Error(`Unknown --actions value(s): ${unknown.join(', ')}. Valid actions: ${[...VALID_ACTIONS].join(', ')}`);
  }
  return actions;
}

function parseArgs(argv) {
  const flags = {
    dryRun: true,
    limit: 10,
    actions: null,
    out: null,
    buildCheck: false,
  };
  const positional = [];
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === '--dry-run') flags.dryRun = true;
    else if (arg === '--write') flags.dryRun = false;
    else if (arg === '--limit') flags.limit = Number(argv[++i]);
    else if (arg === '--actions') flags.actions = parseActionList(argv[++i]);
    else if (arg === '--out') flags.out = argv[++i];
    else if (arg === '--build-check') flags.buildCheck = true;
    else if (arg === '--help' || arg === '-h') {
      printUsage();
      process.exit(0);
    } else if (!arg.startsWith('-')) positional.push(arg);
    else throw new Error(`Unknown flag: ${arg}`);
  }
  if (!Number.isFinite(flags.limit) || flags.limit <= 0) throw new Error('--limit must be a positive number');
  return { flags, positional };
}

function toPosix(value) {
  return value.replace(/\\/g, '/');
}

function isExportableName(name) {
  return name === 'default' || /^[A-Za-z_$][\w$]*$/.test(name);
}

function identifierFromPath(filePath, fallback = 'Recovered') {
  const tokens = path.basename(filePath, '.js').match(/[A-Za-z0-9_$]+/g) || [];
  const name = tokens
    .map((token) => token.charAt(0).toUpperCase() + token.slice(1))
    .join('');
  const candidate = name || fallback;
  return /^[A-Za-z_$][\w$]*$/.test(candidate) ? candidate : fallback;
}

function relativeImport(fromFile, toFile) {
  let rel = toPosix(path.relative(path.dirname(fromFile), toFile));
  if (!rel.startsWith('.')) rel = `./${rel}`;
  return rel;
}

function header(candidate, mode) {
  return [
    '/*',
    ' * Generated by jsmap promote-apply.',
    ` * Mode: ${mode}`,
    ` * Action: ${candidate.recommendedAction}`,
    ` * Source part: ${candidate.file}`,
    ` * Score: ${candidate.score}`,
    ` * Readiness: ${candidate.readiness}`,
    ' *',
    ' * Review before wiring this into application imports. The linked runtime',
    ' * remains the source of truth until browser/Vite validation passes.',
    ' */',
    '',
  ].join('\n');
}

function exportFacade(candidate, targetFile, root, mode) {
  const entryFile = path.join(root, 'src/recovered-entry', candidate.entry);
  const importPath = relativeImport(targetFile, entryFile);
  const exports = (candidate.exports || []).filter(isExportableName);
  const lines = [header(candidate, mode)];
  if (exports.length === 0) {
    lines.push(`import * as recovered from '${importPath}';`);
    lines.push('');
    lines.push('export { recovered };');
  } else {
    const nonDefault = exports.filter((name) => name !== 'default');
    if (nonDefault.length > 0) {
      lines.push(`export { ${nonDefault.join(', ')} } from '${importPath}';`);
    }
    if (exports.includes('default')) {
      lines.push(`export { default } from '${importPath}';`);
    }
  }
  lines.push('');
  return lines.join('\n');
}

function scopeWrapper(candidate, mode) {
  const factoryName = `create${identifierFromPath(candidate.suggestedModulePath)}Scope`;
  const deps = [...new Set([...(candidate.bundleGlobals || []), ...(candidate.externalIdentifiers || [])])];
  const lines = [header(candidate, mode)];
  lines.push('function unresolvedPromotion(name) {');
  lines.push("  return function unresolvedRecoveredBinding() {");
  lines.push("    throw new Error(`Recovered binding ${name} has not been moved out of bundle scope yet.`);");
  lines.push('  };');
  lines.push('}');
  lines.push('');
  lines.push(`export function ${factoryName}(deps = {}) {`);
  lines.push('  const required = [');
  for (const dep of deps) lines.push(`    ${JSON.stringify(dep)},`);
  lines.push('  ];');
  lines.push('  const missing = required.filter((name) => !(name in deps));');
  lines.push('  if (missing.length > 0) {');
  lines.push("    throw new Error(`Missing recovered scope dependencies: ${missing.join(', ')}`);");
  lines.push('  }');
  lines.push('');
  lines.push('  // TODO: move cohesive functions from the source part into this wrapper');
  lines.push('  // after replacing bundle-scope references with deps.<name>.');
  lines.push('  return {');
  const surface = (candidate.exports?.length ? candidate.exports : candidate.declarations || [])
    .filter(isExportableName)
    .filter((name) => name !== 'default')
    .slice(0, 24);
  for (const name of surface) {
    lines.push(`    ${name}: unresolvedPromotion(${JSON.stringify(name)}),`);
  }
  lines.push('  };');
  lines.push('}');
  lines.push('');
  lines.push('export const jsmapPromotionSource = ' + JSON.stringify({
    sourcePart: candidate.file,
    sourceRange: candidate.sourceRange,
    declarations: candidate.declarations,
    bundleGlobals: candidate.bundleGlobals,
    externalIdentifiers: candidate.externalIdentifiers,
  }, null, 2) + ';');
  lines.push('');
  return lines.join('\n');
}

function runtimeBoundary(candidate, targetFile, root, mode) {
  const entryFile = path.join(root, 'src/recovered-entry', candidate.entry);
  const importPath = relativeImport(targetFile, entryFile);
  const lines = [header(candidate, mode)];
  lines.push('// Runtime boundary scaffolds are metadata-only by default.');
  lines.push('// Importing the recovered entry can execute top-level app/runtime side effects.');
  lines.push('// After reviewing side effects, replace this metadata with a deliberately narrow facade.');
  lines.push('');
  lines.push('export const jsmapRuntimeBoundary = ' + JSON.stringify({
    sourcePart: candidate.file,
    recoveredEntry: importPath,
    exports: candidate.exports,
    runtimeCategories: candidate.runtimeCategories,
    runtimeRoles: candidate.runtimeRoles,
    runtimeSignals: candidate.runtimeSignals,
  }, null, 2) + ';');
  lines.push('');
  return lines.join('\n');
}

function inspectNote(candidate, mode) {
  const lines = [header(candidate, mode)];
  lines.push('export const jsmapInspectionOnly = ' + JSON.stringify({
    sourcePart: candidate.file,
    sourceRange: candidate.sourceRange,
    declarations: candidate.declarations,
    blockers: candidate.blockers,
  }, null, 2) + ';');
  lines.push('');
  return lines.join('\n');
}

function leafModule(candidate, mode) {
  const name = candidate.leafCandidate?.name || candidate.declarations?.[0] || 'recoveredLeaf';
  const params = candidate.leafCandidate?.params || [];
  const lines = [header(candidate, mode)];
  lines.push('export const jsmapLeafCandidate = ' + JSON.stringify({
    sourcePart: candidate.file,
    sourceRange: candidate.sourceRange,
    name,
    params,
    externalIdentifiers: candidate.externalIdentifiers || [],
  }, null, 2) + ';');
  lines.push('');
  const source = candidate.leafCandidate?.source;
  if (source) {
    const exported = source
      .replace(/^(\s*)function\s+/, '$1export function ')
      .replace(/^(\s*)const\s+/, '$1export const ');
    lines.push(exported.trimEnd());
  } else {
    lines.push('// Source text was not included in this plan; copy the listed range after review.');
    lines.push(`export function ${name}(${params.join(', ')}) {`);
    lines.push(`  throw new Error(${JSON.stringify(`Leaf candidate ${name} has not been copied from ${candidate.file} yet.`)});`);
    lines.push('}');
  }
  lines.push('');
  return lines.join('\n');
}

function renderCandidate(candidate, targetFile, root, mode) {
  switch (candidate.recommendedAction) {
    case 'create-export-facade':
      return exportFacade(candidate, targetFile, root, mode);
    case 'create-runtime-export-facade':
      return runtimeBoundary(candidate, targetFile, root, mode);
    case 'create-scope-wrapper':
      return scopeWrapper(candidate, mode);
    case 'extract-leaf-module':
      return leafModule(candidate, mode);
    case 'wrap-runtime-boundary':
      return runtimeBoundary(candidate, targetFile, root, mode);
    case 'wrap-or-replace-vendor':
      return runtimeBoundary(candidate, targetFile, root, mode);
    case 'inspect-only':
    default:
      return inspectNote(candidate, mode);
  }
}

async function writeBuildCheck(root, outputs) {
  const promotedRoot = path.join(root, 'src/promoted');
  const checkFile = path.join(promotedRoot, '__build_check__.js');
  const imports = outputs
    .filter((output) => !output.dryRun && output.written.endsWith('.js'))
    .filter((output) => ['extract-leaf-module', 'create-scope-wrapper', 'inspect-only'].includes(output.action))
    .map((output, index) => {
      const importPath = toPosix(path.relative(path.dirname(checkFile), path.join(root, output.written)));
      const specifier = importPath.startsWith('.') ? importPath : `./${importPath}`;
      return `import * as promoted${index} from '${specifier}';`;
    });
  const lines = [
    '/* Generated by jsmap promote-apply --build-check. */',
    ...imports,
    '',
    `export const promotedBuildCheckCount = ${imports.length};`,
    'export default promotedBuildCheckCount;',
    '',
  ];
  await fsp.mkdir(path.dirname(checkFile), { recursive: true });
  await fsp.writeFile(checkFile, lines.join('\n'), 'utf8');
  return toPosix(path.relative(root, checkFile));
}

async function main() {
  const { flags, positional } = parseArgs(process.argv.slice(2));
  const root = path.resolve(positional[0] || '');
  if (!positional[0]) {
    printUsage();
    process.exitCode = 1;
    return;
  }

  const planFile = path.join(root, 'recovery-promotion-plan.json');
  if (!fs.existsSync(planFile)) {
    throw new Error(`Missing recovery-promotion-plan.json in ${root}. Run jsmap promote-plan first.`);
  }
  const plan = JSON.parse(await fsp.readFile(planFile, 'utf8'));
  let candidates = plan.candidates || [];
  const totalCandidates = candidates.length;
  if (flags.actions) {
    candidates = candidates.filter((candidate) => flags.actions.has(candidate.recommendedAction));
    if (candidates.length === 0) {
      console.warn(`No promotion candidates matched --actions ${[...flags.actions].join(', ')} across ${totalCandidates} planned candidate(s).`);
    }
  }
  candidates = candidates.slice(0, flags.limit);

  const mode = flags.dryRun ? 'dry-run-preview' : 'write';
  const previewRoot = path.resolve(flags.out || path.join(root, '.jsmap-promote-preview'));
  const outputs = [];

  for (const candidate of candidates) {
    const targetPath = path.join(root, candidate.suggestedModulePath);
    const outputPath = flags.dryRun
      ? path.join(previewRoot, candidate.suggestedModulePath)
      : targetPath;
    await fsp.mkdir(path.dirname(outputPath), { recursive: true });
    const content = renderCandidate(candidate, outputPath, root, mode);
    await fsp.writeFile(outputPath, content, 'utf8');
    outputs.push({
      action: candidate.recommendedAction,
      score: candidate.score,
      sourcePart: candidate.file,
      target: toPosix(path.relative(root, targetPath)),
      written: toPosix(path.relative(root, outputPath)),
      dryRun: flags.dryRun,
    });
  }

  const manifest = {
    generatedBy: 'jsmap promote-apply',
    generatedAt: new Date().toISOString(),
    mode,
    root,
    plan: toPosix(path.relative(root, planFile)),
    outputs,
  };
  if (!flags.dryRun && flags.buildCheck) {
    manifest.buildCheck = await writeBuildCheck(root, outputs);
  }
  const manifestPath = flags.dryRun
    ? path.join(previewRoot, 'promotion-apply-preview.json')
    : path.join(root, 'promotion-apply-manifest.json');
  await fsp.mkdir(path.dirname(manifestPath), { recursive: true });
  await fsp.writeFile(manifestPath, JSON.stringify(manifest, null, 2) + '\n', 'utf8');

  console.log(`${flags.dryRun ? 'Previewed' : 'Wrote'} ${outputs.length} promotion scaffold${outputs.length === 1 ? '' : 's'}.`);
  console.log(`Manifest: ${manifestPath}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.stack || error.message : error);
  process.exitCode = 1;
});
